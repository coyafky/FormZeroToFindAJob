// 加法赋值（+=）：将右边的值加到左边的变量上，并将结果赋给左边的变量

let a = 10;
a += 5; //等价于 a=a+5
console.log(a);

// 减法赋值（-=）：将右边的值从左边的变量中减去，并将结果赋给左边的变量。

let b = 19;
b -= 5; // 等价于 b =b-15
console.log(b);

// 乘法赋值（*=）：将左边的变量乘以右边的值，并将结果赋给左边的变量。

let c = 10;
c *= 10; // 等价于 c=c*10
console.log(c);

// 除法赋值（/=）：将左边的变量除以右边的值，并将结果赋给左边的变量。
let d = 10;
d /= 2; // 等价于 d = d / 2
console.log(d); // 5

// 取模赋值（%=）：将左边的变量对右边的值取模，并将结果赋给左边的变量。
let e = 10;
e %= 3; // 等价于 e = e % 3
console.log(e); // 1
