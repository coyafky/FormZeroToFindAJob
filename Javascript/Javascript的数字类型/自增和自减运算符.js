let a = 5;

// 前缀自增
let prefixIncrement = ++a;
console.log(prefixIncrement);
console.log(a);

// 后缀自增
let b = 5;
let postfixIncrement = b++;
console.log(postfixIncrement);
console.log(b);

// 前缀自减少

let c = 5;

let prefixDecrement = --c;
console.log(prefixDecrement);
console.log(c);

// 后缀自减
let d = 5;
let postfixDecrement = d--;
console.log(postfixDecrement);
console.log(d);
