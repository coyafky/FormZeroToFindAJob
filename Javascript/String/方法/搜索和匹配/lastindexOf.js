let arr = [1, 2, 3, 4, 5, 2];

console.log(arr.lastIndexOf(2)); //5
console.log(arr.lastIndexOf(2, 4)); //1
console.log(arr.lastIndexOf(6)); //-1
// lastIndexOf(2, 4) 中：为什么输出结果是1 而不是5

// 2 是要搜索的元素。
// 4 是开始搜索的索引位置。
// 从索引 4 开始向前搜索数组 [1, 2, 3, 4, 5, 2]，找到的第一个 2 的索引是 1。

// 详细步骤
// 数组：[1, 2, 3, 4, 5, 2]
// 开始索引：4
// 搜索方向：从索引 4 开始向前搜索。
// 从索引 4 开始向前搜索，依次检查每个元素：

// 索引 4：元素是 5，不是 2。
// 索引 3：元素是 4，不是 2。
// 索引 2：元素是 3，不是 2。
// 索引 1：元素是 2，找到匹配项。
