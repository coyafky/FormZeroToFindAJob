let currentDate = new Date();

console.log('月份' + currentDate.getMonth());
console.log('年份' + currentDate.getFullYear());
console.log('日期' + currentDate.getDate());
console.log('小时' + currentDate.getHours());
console.log('分钟' + currentDate.getMinutes());
console.log('秒' + currentDate.getSeconds());
console.log('毫秒: ' + currentDate.getMilliseconds()); // 例如：789

// 获取星期几

console.log('星期几' + currentDate.getDay());
// 获取时间挫

console.log(currentDate.getTime());
