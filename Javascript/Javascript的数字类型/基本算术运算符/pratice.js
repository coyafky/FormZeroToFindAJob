console.log('减法: ' + subtraction); // 7
console.log('乘法: ' + multiplication); // 30
console.log('除法: ' + division); // 3.3333333333333335
console.log('取模: ' + modulus); // 1
console.log('幂运算: ' + exponentiation); // 1000
